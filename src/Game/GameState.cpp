/*virtual*/ GameState::~GameState(void)
{

}

//--------------------------------------------------------------------------------------------------
/// @todo comment
//--------------------------------------------------------------------------------------------------
void GameState::Update(void)
{
	float current = CShTimer::GetInstance()->GetCurrent();
	float dt = (current - m_fLastUpdateTime) / 1000.0f;
	m_fLastUpdateTime = current;

	Update(dt);
}

//--------------------------------------------------------------------------------------------------
/// @todo comment
//--------------------------------------------------------------------------------------------------
void GameState::ResetActivationTime(void)
{
	m_fLastUpdateTime = CShTimer::GetInstance()->GetCurrent();
}